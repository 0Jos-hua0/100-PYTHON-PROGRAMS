# Title: Python Variables
# This program demonstrates the use of variables in Python.

# An integer variable
age = 30
print("Age:", age)

# A floating-point variable
price = 19.99
print("Price:", price)

# A string variable
name = "John Doe"
print("Name:", name)

# A boolean variable
is_student = True
print("Is a student:", is_student)

# Variables can be updated
age = 31
print("Next year's age:", age)

