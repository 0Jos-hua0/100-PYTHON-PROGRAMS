# Title: Basic Arithmetic Operations
# This program demonstrates basic arithmetic operations in Python.

a = 15
b = 4

# Addition
addition = a + b
print(f"{a} + {b} = {addition}")

# Subtraction
subtraction = a - b
print(f"{a} - {b} = {subtraction}")

# Multiplication
multiplication = a * b
print(f"{a} * {b} = {multiplication}")

# Division (results in a float)
division = a / b
print(f"{a} / {b} = {division}")

# Modulus (remainder of the division)
modulus = a % b
print(f"{a} % {b} = {modulus}")

# Floor Division (division that results in a whole number, rounded down)
floor_division = a // b
print(f"{a} // {b} = {floor_division}")

# Exponentiation (a to the power of b)
exponentiation = 2 ** 3 # 2*2*2
print(f"2 ** 3 = {exponentiation}")
