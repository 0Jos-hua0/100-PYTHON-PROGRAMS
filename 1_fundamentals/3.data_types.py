# Title: Python Data Types
# This program demonstrates various data types in Python.

# String (str)
my_string = "Hello, Python!"
print("String:", my_string, "Type:", type(my_string))

# Integer (int)
my_integer = 100
print("Integer:", my_integer, "Type:", type(my_integer))

# Float (float)
my_float = 3.14
print("Float:", my_float, "Type:", type(my_float))

# Boolean (bool)
my_boolean = False
print("Boolean:", my_boolean, "Type:", type(my_boolean))

# List (list) - ordered and mutable
my_list = ["apple", "banana", "cherry"]
print("List:", my_list, "Type:", type(my_list))

# Tuple (tuple) - ordered and immutable
my_tuple = ("red", "green", "blue")
print("Tuple:", my_tuple, "Type:", type(my_tuple))

# Dictionary (dict) - unordered and mutable key-value pairs
my_dict = {"name": "Alice", "age": 25}
print("Dictionary:", my_dict, "Type:", type(my_dict))

# NoneType (None)
my_none = None
print("NoneType:", my_none, "Type:", type(my_none))
